void main() {
  String beverage = "Coffee";

  switch (beverage) {
    case "Tea":
      print("🍵 Tea: A soothing drink to relax.");
      break;
    case "Coffee":
      print("☕ Coffee: The perfect boost for the day.");
      break;
    case "Juice":
      print("🧃 Juice: Refreshing and full of vitamins.");
      break;
    case "Water":
      print("💧 Water: Stay hydrated and healthy.");
      break;
    default:
      print("🥤 Unknown beverage: Try a new drink today!");
  }
}
